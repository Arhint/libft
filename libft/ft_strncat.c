#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t l;

	i = 0;
	l = ft_strlen(s1);
	while (i < n && s2[i] != '\0')
		s1[l++] = s2[i++];
	s1[l] = '\0';
	return (s1);
}
