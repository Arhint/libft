int		ft_count_words(char const *s, char c)
{
	int i;
	int n;

	i = 0;
	n = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		if (s[i] == c && s[i + 1] != c)
			n++;
		i++;
	}
	if (s[0] != c)
		n++;
	return (n);
}
